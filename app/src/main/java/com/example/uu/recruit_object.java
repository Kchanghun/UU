package com.example.uu;

public class recruit_object {
    private String leader;
    private String currentUserNum;
    private String date;
    private String time;
    private String userInfo;
    private String runningSpeed;

    public recruit_object() { }

    public String getLeader() {
        return leader;
    }

    public void setLeader(String leader) {
        this.leader = leader;
    }

    public String getCurrentUserNum() {
        return currentUserNum;
    }

    public void setCurrentUserNum(String currentUserNum) {
        this.currentUserNum = currentUserNum;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getUserInfo() {
        return userInfo;
    }

    public void setUserInfo(String userInfo) {
        this.userInfo = userInfo;
    }

    public String getRunningSpeed() {
        return runningSpeed;
    }

    public void setRunningSpeed(String runningSpeed) {
        this.runningSpeed = runningSpeed;
    }
}
